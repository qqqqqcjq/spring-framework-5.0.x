	/**
	 * Instantiate and invoke all registered BeanFactoryPostProcessor beans,
	 * respecting explicit order if given.
	 * <p>Must be called before singleton instantiation.
	 */
	protected void invokeBeanFactoryPostProcessors(ConfigurableListableBeanFactory beanFactory) {
		//getBeanFactoryPostProcessors（）这个方法是直接获取一个list，这个list是在AnnotationConfigApplicationContext被定义，
		//需要(程序员或者spring内部)调用AnnotationConfigApplicationContext.addBeanFactoryPostProcesor()才可以放进这个list
		//即使加了@companent,getBeanFactoryPostProcessors()也获取不到，对@companent的处理并不会调用AnnotationConfigApplicationContext.addBeanFactoryPostProcesor()，
		//我们自己写的BeanFactoryPostProcessor，需要我们自己调用context.addBeanFactoryPostProcesor(),才可以放进这个list
        //getBeanFactoryPostProcessors()就是为了获取我们自己注册到beanFactory.beanFactoryPostProcessors中的beanFactoryPostProcessor实例
        //spring内部的beanFactoryPostProcessor在这个方法里实例化并放进currentRegistryProcessors局部变量中
        //==============================================================================================================
        //虽然我们自己写的BeanFactoryPostProcessor不会放进这个list, 但是完成扫描后会判断扫描的bean里面有没有BeanFactoryPostProcessor，有的话就执行，也是在下面这个方法里面实现
        //BeanDefinitionRegistryPostProcessor一样，具体看下面这个方法
		PostProcessorRegistrationDelegate.invokeBeanFactoryPostProcessors(beanFactory, getBeanFactoryPostProcessors());

		// Detect a LoadTimeWeaver and prepare for weaving, if found in the meantime
		// (e.g. through an @Bean method registered by ConfigurationClassPostProcessor)
		if (beanFactory.getTempClassLoader() == null && beanFactory.containsBean(LOAD_TIME_WEAVER_BEAN_NAME)) {
			beanFactory.addBeanPostProcessor(new LoadTimeWeaverAwareProcessor(beanFactory));
			beanFactory.setTempClassLoader(new ContextTypeMatchClassLoader(beanFactory.getBeanClassLoader()));
		}
	}

